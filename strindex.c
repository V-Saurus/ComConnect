/* Модуль поиска подстроки в страке с выводом индекса первого совпадения подстроки */
/* взят из книги "Язык программирования C, Керниган, Ритчи" */

#include <stdio.h>

int strindex(char s[], char t[]) {
  int i, j, k;

  for (i = 0; s[i] != '\0'; i++) {
    for (j = i, k = 0; (t[k] != '\0') && (s[j] == t[k]); j++, k++) {
//      printf("s[%d]=%d, t[%d]=%d\n", j, s[j], k, t[k]);
      ;
    }
    if ((k > 0) && (t[k] == '\0')) {
      return i;
    }
  }
  return -1;
}
